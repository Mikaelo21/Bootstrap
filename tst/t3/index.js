function copiartexto(){
    var texto = '<div class="container">\n  Conteúdo aqui\n</div>';
    navigator.clipboard.writeText(texto).then(function() {
        document.getElementById("alerta").innerHTML = '<div class="alert alert-success alert-dismissible fade show w-100 mt-2 divalert" role="alert"> Código copiado para a área de transferência!<button type="button" class="btn-close" data-bs-dismiss="alert" aria-label="Close"></button></div>';
        fechaalerta();
    }).catch(function(err) {
        document.getElementById("alerta").innerHTML = '<div class="alert alert-danger alert-dismissible fade show w-100 mt-2 divalert" role="alert">❌ Erro ao copiar o código!<button type="button" class="btn-close" data-bs-dismiss="alert" aria-label="Close"></button></div>';
    });
}
